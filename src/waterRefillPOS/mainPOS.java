    //FUNCTION TO CHECK STOCKS LEFT AND/OR ADD ORDER TO THE JTABLE
    private void checkstocks()
    {
        String name= txtcode.getText();
        String prod = txtpname.getText(); 
     
        
        try {
//             Class.forName("com.mysql.jdbc.Driver");
//             connect1 = DriverManager.getConnection("jdbc:mysql://localhost/waterdb", "root", "");
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url  = "jdbc:mysql://localhost:3306/waterdb"
                        + "?useSSL=false&serverTimezone=UTC&allowPublicKeyRetrieval=true";
            String user = "root";
            String pass = "root!";  
            connect1 = DriverManager.getConnection(url, user, pass);
             insert = connect1.prepareStatement("select * from product where code=?");
             insert.setString(1, name);
             rs = insert.executeQuery();
             
             while(rs.next())
             {
                 int DBqty;
                 DBqty = rs.getInt("qty");
                
                 int price = Integer.parseInt(txtprice.getText());
                 int newqty = Integer.parseInt(txtqty.getText());
                 
                 
                 int total=price*newqty;
                 
                 if(newqty > DBqty)
                 {
                     JOptionPane.showMessageDialog(this,"Available product is " + "= " + DBqty + "\nStock quantity is not enough.", "Error", JOptionPane.ERROR_MESSAGE);
                
                 }
                 else
                 {
                     
                     if(DBqty<=10)
                    {
                        JOptionPane.showMessageDialog(this,"LOW STOCKS ALERT!\nProduct:  " + prod + "\nStocks: " + DBqty , "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                     model = (DefaultTableModel)tblorder.getModel();
                     model.addRow( new Object[]
                     {
                         txtcode.getText(),
                         txtpname.getText(),
                         txtprice.getText(),
                         txtqty.getText(),
                         total,
                     });
                     
                     int sum=0;
                     
                     for(int i=0; i<tblorder.getRowCount(); i++)
                     {
                         sum=sum+ Integer.parseInt(tblorder.getValueAt(i, 4).toString());
                         
                     }
                     
                     txttotal.setText(Integer.toString(sum));
                     
                     
                     
                     txtcode.setText("");
                     txtpname.setText("");
                     txtprice.setText("");
                     txtqty.setText("");
                     txtcode.requestFocus();
                     
                 
                 }
             }
             
            
             
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(mainPOS.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(mainPOS.class.getName()).log(Level.SEVERE, null, ex);
        }
               
    }
    
    
    
    
    
    //FUNCTION THAT'S ALL ABOUT INCOME AND STOCKS
    private void income()
    {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        
        String tot = txttotal.getText();
        String pay = txtpayment.getText();
        String change = txtchange.getText();
        int lastInsertId =0;
        
        try {
//            Class.forName("com.mysql.jdbc.Driver");
//            connect1 = DriverManager.getConnection("jdbc:mysql://localhost/waterdb", "root", "");
            Class.forName("com.mysql.cj.jdbc.Driver");
            String url  = "jdbc:mysql://localhost:3306/waterdb"
                        + "?useSSL=false&serverTimezone=UTC&allowPublicKeyRetrieval=true";
            String user = "root";
            String pass = "root!";  
            connect1 = DriverManager.getConnection(url, user, pass);
            //SAVES ALL THE INCOME IN DB INCLUDING ITS DATE,AMOUNT, AND PAYMENT
           Class.forName("com.mysql.jdbc.Driver");
            connect1 = DriverManager.getConnection("jdbc:mysql://localhost/waterdb", "root", "");
            String query = "insert into incomerecords (date,amount,payment,pchange) values(?,?,?,?)";
            insert = connect1.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            
            insert.setString(1, date);
            insert.setString(2, tot);
            insert.setString(3, pay);
            insert.setString(4, change);
            insert.executeUpdate();
            ResultSet genKeyResult = insert.getGeneratedKeys();
        
         if(genKeyResult.next())
         {
             lastInsertId = genKeyResult.getInt(1);
         }
         
         int rows = tblorder.getRowCount();
         
         
         //TO STORE A MORE DETAILED SALES INFO, INCLUDING THE SPECIFIC VARIATION AND QUANTITY
         String query1 = "insert into productsales (sales_id,prduct_id,sell_price,qty, total ) values(?,?,?,?,?)";
         insert = connect1.prepareStatement(query1);
         
         String product_id= "";
         String price= "";
         String qty= "";
         int total= 0;
         
         
         for(int i=0; i<tblorder.getRowCount(); i++)
         {
             product_id = (String)tblorder.getValueAt(i, 0);
             price = (String)tblorder.getValueAt(i, 2);
             qty = (String)tblorder.getValueAt(i, 3);
             total = (int)tblorder.getValueAt(i, 4);
             
             insert.setInt(1, lastInsertId);
             insert.setString(2, product_id);
             insert.setString(3, price);
             insert.setString(4, qty);
             insert.setInt(5, total);
             
             insert.executeUpdate();
         }
        
         //TO KEEP THE QUANTITY OF STOCKS UPDATED IN MYSQL
         String query2 = "update product set qty= qty-? where code=?";
         insert = connect1.prepareStatement(query2);
         
         for(int i=0; i<tblorder.getRowCount(); i++)
         {
             product_id = (String)tblorder.getValueAt(i, 0);
             qty = (String)tblorder.getValueAt(i, 3);
             
             insert.setString(1, qty);
             insert.setString(2, product_id);
             insert.execute();
         }
    
         insert.addBatch();
         JOptionPane.showMessageDialog(this, "Record saved.");
         
         model = (DefaultTableModel)tblorder.getModel();
         model.setRowCount(0);
         txttotal.setText("");
         txtpayment.setText("");
         txtchange.setText("");  
         txtcode.requestFocus();
         
         
         
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(mainPOS.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(mainPOS.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
