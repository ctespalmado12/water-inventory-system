     private void table_update()
    {
        try {
            int i;
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                String url  = "jdbc:mysql://localhost:3306/waterdb"
                            + "?useSSL=false&serverTimezone=UTC&allowPublicKeyRetrieval=true";
                String user = "root";
                String pass = "root!";  
                connect1 = DriverManager.getConnection(url, user, pass);
//                Class.forName("com.mysql.jdbc.Driver");
//                 connect1 = DriverManager.getConnection("jdbc:mysql://localhost/waterdb", "root", "");
                 pst1 = connect1.prepareStatement("select* from brand");
                 ResultSet rs =pst1.executeQuery();
                 
                 ResultSetMetaData rsd = rs.getMetaData();
                 i = rsd.getColumnCount();
                 
                 DefaultTableModel d = (DefaultTableModel)tblCategory.getModel();
                 d.setRowCount(0);
                 
                 while(rs.next())
                 {
                     Vector v2 = new Vector();
                     
                     for(int j=1; j<=i; j++)
                     {
                         v2.add(rs.getString("id"));
                         v2.add(rs.getString("brand"));
                         v2.add(rs.getString("status"));
                     }
                     
                     d.addRow(v2);
                 }           
                 
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(brand.class.getName()).log(Level.SEVERE, null, ex);
            }
     
        } catch (SQLException ex) {
            Logger.getLogger(brand.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    
    
    
    
    
